package com.gym.app.dto;

import java.sql.Date;

public class AndroidUserDto {
	private int unum;
	private String uname;
	private String id;
	private String pwd;
	private String addr;
	private int sex;
	private Date birth;
	private Date regdate;
	private String savefile;
	private String phone;
	private int grade;
	

	public AndroidUserDto() {
	}

	public AndroidUserDto(int unum, String uname, String id, String pwd, String addr, int sex, Date birth, Date regdate,
			String savefile, String phone, int grade ) {
		super();
		this.unum = unum;
		this.uname = uname;
		this.id = id;
		this.pwd = pwd;
		this.addr = addr;
		this.sex = sex;
		this.birth = birth;
		this.regdate = regdate;
		this.savefile = savefile;
		this.phone = phone;
		this.grade = grade;
		
	}

	public int getUnum() {
		return unum;
	}

	public void setUnum(int unum) {
		this.unum = unum;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public Date getBirth() {
		return birth;
	}

	public void setBirth(Date birth) {
		this.birth = birth;
	}

	public Date getRegdate() {
		return regdate;
	}

	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}

	public String getSavefile() {
		return savefile;
	}

	public void setSavefile(String savefile) {
		this.savefile = savefile;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public void setGrade(int grade) {
		this.grade = grade;
	}

	public int getGrade() {
		return grade;
	}

}
