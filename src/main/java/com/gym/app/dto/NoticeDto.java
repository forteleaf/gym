package com.gym.app.dto;

import java.sql.Date;

public class NoticeDto {
	private int ntnum;
	private String id;
	private String title;
	private String content;
	private int hit;
	private Date regdate;
	public NoticeDto() {}
	public NoticeDto(int ntnum, String id, String title, String content, int hit, Date regdate) {
		super();
		this.ntnum = ntnum;
		this.id = id;
		this.title = title;
		this.content = content;
		this.hit = hit;
		this.regdate = regdate;
	}
	public int getNtnum() {
		return ntnum;
	}
	public void setNtnum(int ntnum) {
		this.ntnum = ntnum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getHit() {
		return hit;
	}
	public void setHit(int hit) {
		this.hit = hit;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	
}
