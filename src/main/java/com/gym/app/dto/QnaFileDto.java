package com.gym.app.dto;

import java.sql.Date;

public class QnaFileDto {
	private int qfilenum;
	private int qnanum;
	private String orgfilenum;
	private String savefilenum;
	private long filesize;
	private Date regdate;
	public QnaFileDto() {}
	public QnaFileDto(int qfilenum, int qnanum, String orgfilenum, String savefilenum, long filesize, Date regdate) {
		super();
		this.qfilenum = qfilenum;
		this.qnanum = qnanum;
		this.orgfilenum = orgfilenum;
		this.savefilenum = savefilenum;
		this.filesize = filesize;
		this.regdate = regdate;
	}
	public int getQfilenum() {
		return qfilenum;
	}
	public void setQfilenum(int qfilenum) {
		this.qfilenum = qfilenum;
	}
	public int getQnanum() {
		return qnanum;
	}
	public void setQnanum(int qnanum) {
		this.qnanum = qnanum;
	}
	public String getOrgfilenum() {
		return orgfilenum;
	}
	public void setOrgfilenum(String orgfilenum) {
		this.orgfilenum = orgfilenum;
	}
	public String getSavefilenum() {
		return savefilenum;
	}
	public void setSavefilenum(String savefilenum) {
		this.savefilenum = savefilenum;
	}
	public long getFilesize() {
		return filesize;
	}
	public void setFilesize(long filesize) {
		this.filesize = filesize;
	}
	public Date getRegdate() {
		return regdate;
	}
	public void setRegdate(Date regdate) {
		this.regdate = regdate;
	}
	
}
