package com.gym.app.dto;

public class ExerDto {
	private int exnum;
	private String exname;
	private int exclass;
	private int calorie;
	private int aero;
	private int strength;
	private int stamina;
	private String exerinfo;
	public ExerDto() {}
	public ExerDto(int exnum, String exname, int exclass, int calorie, int aero, int strength, int stamina, String exerinfo) {
		super();
		this.exnum = exnum;
		this.exname = exname;
		this.exclass = exclass;
		this.calorie = calorie;
		this.aero = aero;
		this.strength = strength;
		this.stamina = stamina;
		this.exerinfo = exerinfo;
	}
	
	public String getExerinfo() {
		return exerinfo;
	}
	public void setExerinfo(String exerinfo) {
		this.exerinfo = exerinfo;
	}
	public int getExnum() {
		return exnum;
	}
	public void setExnum(int exnum) {
		this.exnum = exnum;
	}
	public String getExname() {
		return exname;
	}
	public void setExname(String exname) {
		this.exname = exname;
	}
	public int getExclass() {
		return exclass;
	}
	public void setExclass(int exclass) {
		this.exclass = exclass;
	}
	public int getCalorie() {
		return calorie;
	}
	public void setCalorie(int calorie) {
		this.calorie = calorie;
	}
	public int getAero() {
		return aero;
	}
	public void setAero(int aero) {
		this.aero = aero;
	}
	public int getStrength() {
		return strength;
	}
	public void setStrength(int strength) {
		this.strength = strength;
	}
	public int getStamina() {
		return stamina;
	}
	public void setStamina(int stamina) {
		this.stamina = stamina;
	}
	
	
	
}
